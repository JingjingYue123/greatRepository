package com.djcps.crm.commons.fluentvalidator;


/**
 * 该枚举为正则表达式枚举
 * @author 郑天伟
 *
 */
public enum RegExpEnum {
	
	/**
	 * 小写英文单词验证
	 */
	ONLY_LOWER_CASE("\b[a-z]+\b"),
	
	/**
	 * 大写英文单词验证
	 */
	ONLY_UPPSER_CASE("\b[A-Z]+\b"),
	
	/**
	 * 校验英文分隔符
	 */
	CHECKOUT_SEPARATOR("[-,.?:;'\"!`]|(-{2})|(\\.{3})|(\\(\\))|(\\[\\])|({})?"),
	
	/**
	 * 校验中文标点符号
	 */
	CHECKOUT_PUNCTUATION("[，。？：；“” ‘’ ！— …… 、]|(—{2})|(（）)|(【】)|(｛｝)|(《》)?"),
	/**
	 * 匹配字母或数字或下划线（可用于密码或用户名等验证）
	 */
	MATCH_ENGLISH_CHARACTER("\\w+"),
	
	/**
	 * 匹配中文字符
	 */
	MATCH_CHINESE("[\u0391-\uFFE5]+"),
	
	/**
	 * 校验特殊字符
	 */
	CHECKOUT_SPECIAL_CHARACTER("[、_ =\\\\\\ [\\ ] ; ' ，. / ~ ! @ # $ % ^ & * ( ) + | ? > < “ :{}]+"),
	
	/**
	 * 校验是否是正确的日期格式
	 */
	CHECKOUT_DATETIME("((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\._])(10|12|0?[13578])([-\\/\\._])"
					+ "(3[01]|[12][0-9]|0?[1-9])|(((1[8-9]\\d{2})|([2-9]\\d{3}))([-\\/\\."
					+ "_])(11|0?[469])([-\\/\\._])(30|[12][0-9]|0?[1-9])$)|(^((1[8-9]\\d{2}"
					+ ")|([2-9]\\d{3}))([-\\/\\._])(0?2)([-\\/\\._])(2[0-8]|1[0-9]|0?[1-9]))"
					+ "|(^([2468][048]00)([-\\/\\._])(0?2)([-\\/\\._])(29)$)|(^([3579][26]00"
					+ ")([-\\/\\._])(0?2)([-\\/\\._])(29))|(([1][89][0][48])([-\\/\\._])(0?2"
					+ ")([-\\/\\._])(29))|(([2-9][0-9][0][48])([-\\/\\._])(0?2)([-\\/\\._])("
					+ "29))|(([1][89][2468][048])([-\\/\\._])(0?2)([-\\/\\._])(29))|(([2"
					+ "-9][0-9][2468][048])([-\\/\\._])(0?2)([-\\/\\._])(29))|(([1][89][13"
					+ "579][26])([-\\/\\._])(0?2)([-\\/\\._])(29))|(([2-9][0-9][13579][26]"
					+ ")([-\\/\\._])(0?2)([-\\/\\._])(29))"),
	
	/**
	 * 校验邮箱格式
	 */
	CHECKOUT_EMAIL("[A-Za-z0-9\\u4e00-\\u9fa5]+@[a-zA-Z0-9_-]+(\\.[a-zA-Z0-9_-]+)+"),
	
	/**
	 * 校验15位和18位身份证号码
	 */
	CHECKOUT_IDENTITYCARD("(^[1-9]\\d{7}((0\\d)|(1[0-2]))(([0|1|2]\\d)|3[0-1])\\d{3}$)|"
						+ "(^[1-9]\\d{5}[1-9]\\d{3}((0\\d)|(1[0-2]))(([0|1|2]\\d)|3[0-1"
						+ "])((\\d{4})|\\d{3}[Xx])$)"),
	
	/**
	 * 校验手机号
	 */
	CHECKOUT_MOBILEPHONE("1[3,5,8]\\d{9}"),
	
	/**
	 * 校验座机号
	 */
	CHECKOUT_TELEPHONE("(^0\\d{2}-?\\d{8}$)|(^0\\d{3}-?\\d{7}$)|(^0\\d2-?\\d{8}$)|(^0\\d3-?\\d{7}$)")
	;
	
	
	
	private String reg;
		
	private RegExpEnum(String reg) {
		this.reg = reg;
	}

	public String getReg() {
		return reg;
	}

	public void setReg(String reg) {
		this.reg = reg;
	}
	
	
	
}
